/* O que é uma variável? 
   A variável nada mais é do que um NOME dado para uma determinada
   POSIÇÃO de MEMÓRIA para conter um valor de um determinado tipo. */

/* A definição de variáveis segue a seguinte sintaxe: 
   tipo var1, var2, ... varN 
   
   Exemplo: 
   int i;                          (i é uma variável do tipo INTEIRO)
   char ch1, novo_char;            (ch1 e novo_char são variáveis do tipo CHAR)
   float pi, raio, perimetro;      (pi, raio, perímetro são variáveis do tipo FLOAT)
   double total, k123;             (total, k123 são variáveis do tipo DOUBLE) */

/* Obs:
   A declaração de variáveis sempre deve acontecer antes de sua utilização
   e antes de qualquer instrução 
   
   Exemplo:
   
   main()
   {
     Declaração de variáveis

     Instrução 1;
     Instrução 2;
   }   */


